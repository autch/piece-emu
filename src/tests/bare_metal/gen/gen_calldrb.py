#!/usr/bin/env python3
"""Generate call.d %rb (register-indirect delayed call) tests.

Semantics (Class 6A):
  call.d %rb
    SP -= 4
    W[SP] = PC + 4                (skips delay slot)
    PC = rb
  delay-slot instruction executes

Scope:
  - Verify RA = pc+4 (delay slot skipped, resume after it)
  - Verify delay slot always executes regardless of call target
  - Verify multiple sequential call.d %rb calls (register allocation is stable)

Target functions: static naked functions containing just `ret`.  We pass the
function pointer via an inline-asm operand (register) since loading a symbol
via `ld.w %rN, symbol` is target-dependent.

NOTE: jp.d %rb is omitted here — it's flagged as a hardware bug in CLAUDE.md
("delay slot not executed when DMA is active"); verifying emulator behavior
would require a return path from the target, which jp.d does not provide.
"""

from __future__ import annotations

import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent


def case_id(vi: int) -> int:
    return 1 + vi


C_PROLOGUE = """// AUTO-GENERATED by gen_calldrb.py — do not edit by hand.
//
// call.d %rb regression tests.  Each test calls into a naked function via
// register-indirect delayed call and verifies:
//   - the delay slot executed
//   - control returned to pc+4 of the call.d (skipping the delay slot)

#include <stdint.h>

#define CHECK(id, ok) do { if (!(ok)) return (id); } while (0)

__attribute__((naked))
static void target_A(void) { asm("ret\\n"); }

__attribute__((naked))
static void target_B(void) { asm("ret\\n"); }

__attribute__((naked))
static void target_C(void) {
    // Slightly non-trivial target: modifies r4 (caller-saved) before returning.
    asm("ld.w %r4, 0\\n"
        "ret\\n");
}

// Target used to test call.d's RA calculation explicitly:
// stores its entry PC into a memory slot so we can confirm it got called.
static volatile uint32_t target_D_entered;
__attribute__((naked))
static void target_D(void) {
    asm("ld.w %r4, target_D_entered\\n"
        "ld.w %r5, 1\\n"
        "ld.w [%r4], %r5\\n"
        "ret\\n");
}
"""


def emit_basic_call(cid: int, target_name: str) -> str:
    return f"""static int case_{cid}(void) {{
    uint32_t r = 0, s = 0;
    asm volatile(
        "call.d %2\\n"
        "ld.w %1, 1\\n"        // delay slot (always runs)
        "ld.w %0, 1\\n"        // resume at pc+4: r = 1
        : "+r"(r), "+r"(s)
        : "r"(&{target_name})
    );
    CHECK({cid}, r == 1u);
    CHECK({cid}, s == 1u);
    return 0;
}}
"""


def emit_target_entered_check(cid: int) -> str:
    return f"""static int case_{cid}(void) {{
    target_D_entered = 0;
    uint32_t r = 0, s = 0;
    asm volatile(
        "call.d %2\\n"
        "ld.w %1, 1\\n"
        "ld.w %0, 1\\n"
        : "+r"(r), "+r"(s)
        : "r"(&target_D)
        : "memory"
    );
    CHECK({cid}, r == 1u);
    CHECK({cid}, s == 1u);
    CHECK({cid}, target_D_entered == 1u);
    return 0;
}}
"""


def emit_two_sequential_calls(cid: int) -> str:
    return f"""static int case_{cid}(void) {{
    uint32_t r1 = 0, s1 = 0, r2 = 0, s2 = 0;
    asm volatile(
        "call.d %4\\n"
        "ld.w %1, 1\\n"
        "ld.w %0, 1\\n"
        "call.d %5\\n"
        "ld.w %3, 1\\n"
        "ld.w %2, 1\\n"
        : "+r"(r1), "+r"(s1), "+r"(r2), "+r"(s2)
        : "r"(&target_A), "r"(&target_B)
    );
    CHECK({cid}, r1 == 1u);
    CHECK({cid}, s1 == 1u);
    CHECK({cid}, r2 == 1u);
    CHECK({cid}, s2 == 1u);
    return 0;
}}
"""


def emit_nested_call(cid: int) -> str:
    """call.d %rb inside a regular call."""
    return f"""__attribute__((naked))
static uint32_t nested_helper_{cid}(uint32_t target_ptr __attribute__((unused))) {{
    // Args: r12 = target
    // Sets r10 (return) = 0x11 if call.d's resume-after-delay path worked.
    //   - delay slot sets r4 |= 0x10
    //   - post-resume sets r4 |= 0x01
    asm(
        "pushn %r3\\n"
        "ld.w %r4, 0\\n"
        "call.d %r12\\n"
        "or %r4, 16\\n"               // delay slot: r4 |= 0x10
        "or %r4, 1\\n"                // post-resume: r4 |= 0x01
        "ld.w %r10, %r4\\n"
        "popn %r3\\n"
        "ret\\n"
    );
}}

static int case_{cid}(void) {{
    uint32_t r = nested_helper_{cid}((uint32_t)&target_A);
    // If delay slot ran and resume happened: r4 = 0 | 0x10 | 0x01 = 0x11
    CHECK({cid}, r == 0x11u);
    return 0;
}}
"""


C_EPILOGUE_TEMPLATE = """
int main(void) {{
    int e;
{calls}
    return 0;
}}
"""


def main() -> int:
    out = HERE / "test_gen_calldrb.c"
    fns: list[str] = []

    # 1. basic to target_A
    fns.append(emit_basic_call(1, "target_A"))
    # 2. basic to target_B
    fns.append(emit_basic_call(2, "target_B"))
    # 3. basic to target_C (modifies r4 in target)
    fns.append(emit_basic_call(3, "target_C"))
    # 4. target entered flag (memory side-effect)
    fns.append(emit_target_entered_check(4))
    # 5. two sequential calls
    fns.append(emit_two_sequential_calls(5))
    # 6. nested
    fns.append(emit_nested_call(6))

    import re
    case_re = re.compile(r"static int case_(\d+)\(void\)")
    cids = [int(m.group(1)) for c in fns for m in [case_re.search(c)] if m]
    calls = [f"    if ((e = case_{cid}())) return e;" for cid in cids]

    out.write_text(
        C_PROLOGUE + "\n".join(fns)
        + C_EPILOGUE_TEMPLATE.format(calls="\n".join(calls)),
        encoding="utf-8",
    )
    print(f"# wrote {out} : {len(cids)} cases", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
