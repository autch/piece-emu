#!/usr/bin/env python3
"""Generate interrupt/trap regression tests for piece-emu.

Instructions under test (Class 0a):
  int imm2             SW exception 0..3
                       - SP -= 4; W[SP] = pc+2
                       - SP -= 4; W[SP] = PSR
                       - PC = vector
                       - IE = 0
  reti                 Return from interrupt
                       - PSR = W[SP]; SP += 4
                       - PC = W[SP]; SP += 4

Per the CPU manual, SW exceptions 0..3 live at base + 48..60 (trap numbers
12..15).  That is, `int 0` should read the vector at `ttbr + 48`.

Test strategy:
  - Install our handler address at ALL 16 trap vector slots in the table (to
    accommodate emulator-side mapping).  The test passes if the handler is
    reached for each `int imm2`.
  - Also test one case where ONLY the spec-correct vector (base + 48 + imm2*4)
    is installed — this isolates whether `int N` reads from the correct slot.
  - Handler: naked function that bumps a counter, then executes reti.
  - Verify: counter incremented, SP balanced, execution resumed after int.

Trap table base (ttbr) is 0x400 in piece-emu default config.  The test code
(loaded into SRAM) writes handler addresses into IRAM at 0x400 onwards.
"""

from __future__ import annotations

import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent


C_PROLOGUE = r"""// AUTO-GENERATED by gen_trap.py — do not edit by hand.
//
// Interrupt/trap regression tests.  Installs handler addresses into the
// trap table (ttbr = 0x400) and triggers int imm2, verifying handler
// runs and reti returns cleanly.

#include <stdint.h>

#define CHECK(id, ok) do { if (!(ok)) return (id); } while (0)

#define TTBR 0x400u
#define TRAP_TABLE_ENTRIES 16

static volatile uint32_t handler_counter;

// Handler: bumps counter at COUNTER_ADDR slot, then reti.  Saves/restores all
// GPRs.  The counter addresses are passed via fixed memory slots so the
// handler doesn't need inline-asm operand threading.
//
// Convention:
//   BUMP_ADDR_SLOT (0x600) holds address of the counter the handler should bump.
//   BUMP_ADDR_SLOT2 (0x604) holds address for trap_handler_spec.
#define BUMP_ADDR_SLOT  ((volatile uint32_t *)0x600)
#define BUMP_ADDR_SLOT2 ((volatile uint32_t *)0x604)

// Handler uses [%rb + ext] form to access the counter-address slot without
// needing a symbol relocation.  The slot lives at fixed IRAM address 0x600;
// we load 0x600 into r4 via ld.w-with-ext of the raw literal.
extern void trap_handler(void);
__attribute__((naked))
void trap_handler(void) {
    asm(
        "pushn %r15\n"
        "ld.w %r4, 0\n"                  // r4 = 0
        "ext 0x18\n"                     // (0x18<<6)|0 = 0x600
        "add %r4, 0\n"                   // r4 = 0x600
        "ld.w %r4, [%r4]\n"              // r4 = *0x600 = counter address
        "ld.w %r5, [%r4]\n"              // r5 = *counter
        "add %r5, 1\n"
        "ld.w [%r4], %r5\n"              // *counter += 1
        "popn %r15\n"
        "reti\n"
    );
}

static volatile uint32_t spec_handler_counter;
extern void trap_handler_spec(void);
__attribute__((naked))
void trap_handler_spec(void) {
    asm(
        "pushn %r15\n"
        "ld.w %r4, 0\n"
        "ext 0x18\n"                     // (0x18<<6)|4 = 0x604
        "add %r4, 4\n"                   // r4 = 0x604
        "ld.w %r4, [%r4]\n"              // r4 = *0x604 = spec counter address
        "ld.w %r5, [%r4]\n"
        "add %r5, 1\n"
        "ld.w [%r4], %r5\n"
        "popn %r15\n"
        "reti\n"
    );
}

static void install_handler_all(uint32_t handler) {
    volatile uint32_t *ttbl = (volatile uint32_t *)TTBR;
    for (int i = 0; i < TRAP_TABLE_ENTRIES; i++)
        ttbl[i] = handler;
}

static void install_handler_sw(int imm2, uint32_t handler) {
    // SW exception N lives at ttbr + 48 + N*4 per the manual (trap no = 12+N).
    volatile uint32_t *ttbl = (volatile uint32_t *)TTBR;
    ttbl[12 + imm2] = handler;
}

static void install_handler_at(int trap_no, uint32_t handler) {
    volatile uint32_t *ttbl = (volatile uint32_t *)TTBR;
    ttbl[trap_no] = handler;
}
"""


C_EPILOGUE_TEMPLATE = """
int main(void) {{
    int e;
{calls}
    return 0;
}}
"""


def emit_basic_int(cid: int, imm2: int) -> str:
    """All 16 vectors point to trap_handler; execute int imm2; verify bump."""
    return f"""static int case_{cid}(void) {{
    handler_counter = 0;
    *BUMP_ADDR_SLOT  = (uint32_t)(uintptr_t)&handler_counter;
    *BUMP_ADDR_SLOT2 = (uint32_t)(uintptr_t)&spec_handler_counter;
    install_handler_all((uint32_t)&trap_handler);
    asm volatile("int {imm2}\\n" ::: "memory");
    CHECK({cid}, handler_counter == 1u);
    return 0;
}}
"""


def emit_diagnose_vector(cid: int, imm2: int) -> str:
    """Diagnose which trap slot `int imm2` actually reads.

    Install handler at slot `imm2` (= raw trap number) and a DIFFERENT
    handler at slot `12 + imm2` (spec-correct SW exception location).
    Zero every other slot so the CPU faults if a wrong slot is used.
    See which counter bumped.
    """
    return f"""static int case_{cid}(void) {{
    handler_counter = 0;
    spec_handler_counter = 0;
    *BUMP_ADDR_SLOT  = (uint32_t)(uintptr_t)&handler_counter;
    *BUMP_ADDR_SLOT2 = (uint32_t)(uintptr_t)&spec_handler_counter;
    install_handler_all(0u);    // zero all slots first
    install_handler_at({imm2}, (uint32_t)&trap_handler);       // raw slot
    install_handler_sw({imm2}, (uint32_t)&trap_handler_spec);  // spec slot (12+imm2)
    asm volatile("int {imm2}\\n" ::: "memory");
    // Exactly one of the two counters should be 1; the other 0.
    // Spec behavior: spec_handler_counter == 1 && handler_counter == 0
    // Buggy direct-mapping: handler_counter == 1 && spec_handler_counter == 0
    CHECK({cid}, spec_handler_counter + handler_counter == 1u);  // SOMEthing fired
    CHECK({cid}, spec_handler_counter == 1u);                    // SPEC vector used
    return 0;
}}
"""


def emit_int_spec_vector(cid: int, imm2: int) -> str:
    """Install handler ONLY at the spec-correct SW exception vector for imm2.

    If emulator correctly maps `int N` → trap number (12 + N), this works.
    If emulator uses N directly as the trap number, the handler is never
    reached (control jumps to whatever is at ttbr + N*4, typically 0 = reset
    or undefined memory).  This test FAILS only if the mapping is wrong.

    We install a distinct "spec" handler at the correct vector and the plain
    handler at all the OTHER vectors, so that we can tell which vector fired.
    """
    return f"""static int case_{cid}(void) {{
    handler_counter = 0;
    spec_handler_counter = 0;
    *BUMP_ADDR_SLOT  = (uint32_t)(uintptr_t)&handler_counter;
    *BUMP_ADDR_SLOT2 = (uint32_t)(uintptr_t)&spec_handler_counter;
    // Install plain handler everywhere (so any trap number gets caught)
    install_handler_all((uint32_t)&trap_handler);
    // Override the spec-correct SW exception vector with the "spec" handler
    install_handler_sw({imm2}, (uint32_t)&trap_handler_spec);
    asm volatile("int {imm2}\\n" ::: "memory");
    CHECK({cid}, spec_handler_counter == 1u);   // spec vector was used
    CHECK({cid}, handler_counter == 0u);        // wrong vector was NOT used
    return 0;
}}
"""


def emit_sp_balance(cid: int, imm2: int) -> str:
    """Verify SP is unchanged across int/reti round-trip."""
    return f"""static int case_{cid}(void) {{
    handler_counter = 0;
    *BUMP_ADDR_SLOT  = (uint32_t)(uintptr_t)&handler_counter;
    *BUMP_ADDR_SLOT2 = (uint32_t)(uintptr_t)&spec_handler_counter;
    install_handler_all((uint32_t)&trap_handler);
    uint32_t sp_before, sp_after;
    asm volatile(
        "ld.w %0, %%sp\\n"
        "int {imm2}\\n"
        "ld.w %1, %%sp\\n"
        : "=r"(sp_before), "=r"(sp_after)
        :
        : "memory"
    );
    CHECK({cid}, handler_counter == 1u);
    CHECK({cid}, sp_before == sp_after);
    return 0;
}}
"""


def emit_psr_restored(cid: int, imm2: int) -> str:
    """Verify PSR is restored (IE must return to its pre-int value)."""
    return f"""static int case_{cid}(void) {{
    handler_counter = 0;
    *BUMP_ADDR_SLOT  = (uint32_t)(uintptr_t)&handler_counter;
    *BUMP_ADDR_SLOT2 = (uint32_t)(uintptr_t)&spec_handler_counter;
    install_handler_all((uint32_t)&trap_handler);
    uint32_t psr_before, psr_after;
    // Pre-set PSR with IE=1 + some flag bits
    asm volatile(
        "ld.w %%psr, %2\\n"
        "ld.w %0, %%psr\\n"
        "int {imm2}\\n"
        "ld.w %1, %%psr\\n"
        : "=r"(psr_before), "=r"(psr_after)
        : "r"((uint32_t)0x1Au)        // IE=1, V=1, Z=1
        : "memory"
    );
    CHECK({cid}, handler_counter == 1u);
    CHECK({cid}, (psr_after & 0x1Fu) == (psr_before & 0x1Fu));
    return 0;
}}
"""


def main() -> int:
    out = HERE / "test_gen_trap.c"
    cases: list[str] = []
    cid = 1

    # Basic int 0..3 roundtrip
    for imm2 in range(4):
        cases.append(emit_basic_int(cid, imm2))
        cid += 1

    # Spec-vector test: requires emulator to use SW exception slot (12+imm2)
    for imm2 in range(4):
        cases.append(emit_int_spec_vector(cid, imm2))
        cid += 1

    # SP balance
    for imm2 in range(4):
        cases.append(emit_sp_balance(cid, imm2))
        cid += 1

    # PSR restored
    for imm2 in range(4):
        cases.append(emit_psr_restored(cid, imm2))
        cid += 1

    import re
    case_re = re.compile(r"static int case_(\d+)\(void\)")
    cids = [int(m.group(1)) for c in cases for m in [case_re.search(c)] if m]
    calls = [f"    if ((e = case_{cid}())) return e;" for cid in cids]

    out.write_text(
        C_PROLOGUE + "\n" + "\n".join(cases)
        + C_EPILOGUE_TEMPLATE.format(calls="\n".join(calls)),
        encoding="utf-8",
    )
    print(f"# wrote {out} : {len(cids)} cases", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
